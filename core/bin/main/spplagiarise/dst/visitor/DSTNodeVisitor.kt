package spplagiarise.dst.visitor

import spplagiarise.dst.*

interface DSTNodeVisitor {
    fun visitDSTKnownSimpleName(node: DSTKnownSimpleName) {}
    fun visitDSTDeferredSimpleName(node: DSTDeferredSimpleName) {}
    fun visitDSTQualifiedName(node: DSTQualifiedName) {}
    fun visitDSTMethodCall(node: DSTMethodCall) {}
    fun visitDSTConstructorCall(node: DSTConstructorCall) {}
    fun visitDSTAssignment(node: DSTAssignment) {}
    fun visitDSTPrefixExpression(node: DSTPrefixExpression) {}
    fun visitDSTMultiInfixExpression(node: DSTMultiInfixExpression) {}
    fun visitDSTSingleInfixExpression(node: DSTSingleInfixExpression) {}
    fun visitDSTPostfixExpression(node: DSTPostfixExpression) {}
    fun visitDSTVariableDeclarationExpression(node: DSTVariableDeclarationExpression) {}
    fun visitDSTArrayAccess(node: DSTArrayAccess) {}
    fun visitDSTParenthesisedExpression(node: DSTParenthesisedExpression) {}
    fun visitDSTCastExpression(node: DSTCastExpression) {}
    fun visitDSTArrayCreation(node: DSTArrayCreation) {}
    fun visitDSTArrayInitialiser(node: DSTArrayInitialiser) {}
    fun visitDSTConditionalExpression(node: DSTConditionalExpression) {}
    fun visitDSTFieldAccessExpression(node: DSTFieldAccessExpression) {}
    fun visitDSTSuperFieldAccessExpression(node: DSTSuperFieldAccessExpression) {}
    fun visitDSTThisExpression(node: DSTThisExpression) {}
    fun visitDSTInstanceOfExpression(node: DSTInstanceOfExpression) {}
    fun visitDSTLambdaExpression(node: DSTLambdaExpression) {}
    fun visitDSTCreationRefExpression(node: DSTCreationRefExpression) {}
    fun visitDSTTypeMethodRefExpression(node: DSTTypeMethodRefExpression) {}
    fun visitDSTSuperMethodRefExpression(node: DSTSuperMethodRefExpression) {}
    fun visitDSTExpressionMethodRefExpression(node: DSTExpressionMethodRefExpression) {}
    fun visitDSTStringLiteral(node: DSTStringLiteral) {}
    fun visitDSTCharLiteral(node: DSTCharLiteral) {}
    fun visitDSTBooleanLiteral(node: DSTBooleanLiteral) {}
    fun visitDSTNumberLiteral(node: DSTNumberLiteral) {}
    fun visitDSTTypeLiteral(node: DSTTypeLiteral) {}
    fun visitDSTNullLiteral(node: DSTNullLiteral) {}
    fun visitDSTAnonymousClassBody(node: DSTAnonymousClassBody) {}
    fun visitDSTSimpleType(node: DSTSimpleType) {}
    fun visitDSTArrayType(node: DSTArrayType) {}
    fun visitDSTParameterisedType(node: DSTParameterisedType) {}
    fun visitDSTQualifiedType(node: DSTQualifiedType) {}
    fun visitDSTNameQualifiedType(node: DSTNameQualifiedType) {}
    fun visitDSTWildcardType(node: DSTWildcardType) {}
    fun visitDSTUnionType(node: DSTUnionType) {}
    fun visitDSTIntersectionType(node: DSTIntersectionType) {}
    fun visitDSTTypeParameter(node: DSTTypeParameter) {}
    fun visitDSTCompilationUnit(node: DSTCompilationUnit) {}
    fun visitDSTPackageDeclaration(node: DSTPackageDeclaration) {}
    fun visitDSTSingleImportDeclaration(node: DSTSingleImportDeclaration) {}
    fun visitDSTPackageImportDeclaration(node: DSTPackageImportDeclaration) {}
    fun visitDSTStaticFieldImportDeclaration(node: DSTStaticFieldImportDeclaration) {}
    fun visitDSTStaticMethodImportDeclaration(node: DSTStaticMethodImportDeclaration) {}
    fun visitDSTStaticAsterixImportDeclaration(node: DSTStaticAsterixImportDeclaration) {}
    fun visitDSTFieldDeclaration(node: DSTFieldDeclaration) {}
    fun visitDSTClassOrInterfaceTypeDeclaration(node: DSTClassOrInterfaceTypeDeclaration) {}
    fun visitDSTEnumTypeDeclaration(node: DSTEnumTypeDeclaration) {}
    fun visitDSTAnnotationTypeDeclaration(node: DSTAnnotationTypeDeclaration) {}
    fun visitDSTEnumConstant(node: DSTEnumConstant) {}
    fun visitDSTAnnotationMember(node: DSTAnnotationMember) {}
    fun visitDSTFieldGroup(node: DSTFieldGroup) {}
    fun visitDSTConstructorDeclaration(node: DSTConstructorDeclaration) {}
    fun visitDSTMethodDeclaration(node: DSTMethodDeclaration) {}
    fun visitDSTInitialiser(node: DSTInitialiser) {}
    fun visitDSTParameter(node: DSTParameter) {}
    fun visitDSTDimension(node: DSTDimension) {}
    fun visitDSTModifier(node: DSTModifier) {}
    fun visitDSTBlockStatement(node: DSTBlockStatement) {}
    fun visitDSTExpressionStatement(node: DSTExpressionStatement) {}
    fun visitDSTLocalVariableDeclarationGroup(node: DSTLocalVariableDeclarationGroup) {}
    fun visitDSTLocalVariableDeclaration(node: DSTLocalVariableDeclaration) {}
    fun visitDSTSingleVariableDeclaration(node: DSTSingleVariableDeclaration) {}
    fun visitDSTWhileStatement(node: DSTWhileStatement) {}
    fun visitDSTDoWhileStatement(node: DSTDoWhileStatement) {}
    fun visitDSTForStatement(node: DSTForStatement) {}
    fun visitDSTForEachStatement(node: DSTForEachStatement) {}
    fun visitDSTSuperMethodCall(node: DSTSuperMethodCall) {}
    fun visitDSTSuperConstructorCall(node: DSTSuperConstructorCall) {}
    fun visitDSTThisConstructorCall(node: DSTThisConstructorCall) {}
    fun visitDSTSynchronisedStatement(node: DSTSynchronisedStatement) {}
    fun visitDSTLabeledStatement(node: DSTLabeledStatement) {}
    fun visitDSTBreakStatement(node: DSTBreakStatement) {}
    fun visitDSTContinueStatement(node: DSTContinueStatement) {}
    fun visitDSTReturnStatement(node: DSTReturnStatement) {}
    fun visitDSTThrowStatement(node: DSTThrowStatement) {}
    fun visitDSTIfStatement(node: DSTIfStatement) {}
    fun visitDSTSwitchStatement(node: DSTSwitchStatement) {}
    fun visitDSTSwitchCase(node: DSTSwitchCase) {}
    fun visitDSTTryStatement(node: DSTTryStatement) {}
    fun visitDSTEmptyStatement(node: DSTEmptyStatement) {}
    fun visitDSTTypeDeclarationStatement(node: DSTTypeDeclarationStatement) {}
    fun visitDSTAssertStatement(node: DSTAssertStatement) {}
    fun visitDSTCatchClause(node: DSTCatchClause) {}
    fun visitDSTSyntheticMethodCall(node: DSTSyntheticMethodCall) {}
    fun visitDSTSyntheticMethodDeclaration(node: DSTSyntheticMethodDeclaration) {}
    fun visitDSTSyntheticField(dstSyntheticField: DSTSyntheticField) {}
    fun visitDSTSyntheticName(dstSyntheticName: DSTSyntheticName) {}
}

fun DSTNode.accept(visitor: DSTNodeVisitor) {
    when (this) {
        is DSTKnownSimpleName -> visitor.visitDSTKnownSimpleName(this)
        is DSTDeferredSimpleName -> visitor.visitDSTDeferredSimpleName(this)
        is DSTQualifiedName -> visitor.visitDSTQualifiedName(this)
        is DSTMethodCall -> visitor.visitDSTMethodCall(this)
        is DSTConstructorCall -> visitor.visitDSTConstructorCall(this)
        is DSTAssignment -> visitor.visitDSTAssignment(this)
        is DSTPrefixExpression -> visitor.visitDSTPrefixExpression(this)
        is DSTMultiInfixExpression -> visitor.visitDSTMultiInfixExpression(this)
        is DSTSingleInfixExpression -> visitor.visitDSTSingleInfixExpression(this)
        is DSTPostfixExpression -> visitor.visitDSTPostfixExpression(this)
        is DSTVariableDeclarationExpression -> visitor.visitDSTVariableDeclarationExpression(this)
        is DSTArrayAccess -> visitor.visitDSTArrayAccess(this)
        is DSTParenthesisedExpression -> visitor.visitDSTParenthesisedExpression(this)
        is DSTCastExpression -> visitor.visitDSTCastExpression(this)
        is DSTArrayCreation -> visitor.visitDSTArrayCreation(this)
        is DSTArrayInitialiser -> visitor.visitDSTArrayInitialiser(this)
        is DSTConditionalExpression -> visitor.visitDSTConditionalExpression(this)
        is DSTFieldAccessExpression -> visitor.visitDSTFieldAccessExpression(this)
        is DSTSuperFieldAccessExpression -> visitor.visitDSTSuperFieldAccessExpression(this)
        is DSTThisExpression -> visitor.visitDSTThisExpression(this)
        is DSTInstanceOfExpression -> visitor.visitDSTInstanceOfExpression(this)
        is DSTLambdaExpression -> visitor.visitDSTLambdaExpression(this)
        is DSTCreationRefExpression -> visitor.visitDSTCreationRefExpression(this)
        is DSTTypeMethodRefExpression -> visitor.visitDSTTypeMethodRefExpression(this)
        is DSTSuperMethodRefExpression -> visitor.visitDSTSuperMethodRefExpression(this)
        is DSTExpressionMethodRefExpression -> visitor.visitDSTExpressionMethodRefExpression(this)
        is DSTStringLiteral -> visitor.visitDSTStringLiteral(this)
        is DSTCharLiteral -> visitor.visitDSTCharLiteral(this)
        is DSTBooleanLiteral -> visitor.visitDSTBooleanLiteral(this)
        is DSTNumberLiteral -> visitor.visitDSTNumberLiteral(this)
        is DSTTypeLiteral -> visitor.visitDSTTypeLiteral(this)
        is DSTNullLiteral -> visitor.visitDSTNullLiteral(this)
        is DSTAnonymousClassBody -> visitor.visitDSTAnonymousClassBody(this)
        is DSTSimpleType -> visitor.visitDSTSimpleType(this)
        is DSTArrayType -> visitor.visitDSTArrayType(this)
        is DSTParameterisedType -> visitor.visitDSTParameterisedType(this)
        is DSTQualifiedType -> visitor.visitDSTQualifiedType(this)
        is DSTNameQualifiedType -> visitor.visitDSTNameQualifiedType(this)
        is DSTWildcardType -> visitor.visitDSTWildcardType(this)
        is DSTUnionType -> visitor.visitDSTUnionType(this)
        is DSTIntersectionType -> visitor.visitDSTIntersectionType(this)
        is DSTTypeParameter -> visitor.visitDSTTypeParameter(this)
        is DSTCompilationUnit -> visitor.visitDSTCompilationUnit(this)
        is DSTPackageDeclaration -> visitor.visitDSTPackageDeclaration(this)
        is DSTSingleImportDeclaration -> visitor.visitDSTSingleImportDeclaration(this)
        is DSTPackageImportDeclaration -> visitor.visitDSTPackageImportDeclaration(this)
        is DSTStaticFieldImportDeclaration -> visitor.visitDSTStaticFieldImportDeclaration(this)
        is DSTStaticMethodImportDeclaration -> visitor.visitDSTStaticMethodImportDeclaration(this)
        is DSTStaticAsterixImportDeclaration -> visitor.visitDSTStaticAsterixImportDeclaration(this)
        is DSTFieldDeclaration -> visitor.visitDSTFieldDeclaration(this)
        is DSTClassOrInterfaceTypeDeclaration -> visitor.visitDSTClassOrInterfaceTypeDeclaration(this)
        is DSTEnumTypeDeclaration -> visitor.visitDSTEnumTypeDeclaration(this)
        is DSTAnnotationTypeDeclaration -> visitor.visitDSTAnnotationTypeDeclaration(this)
        is DSTEnumConstant -> visitor.visitDSTEnumConstant(this)
        is DSTAnnotationMember -> visitor.visitDSTAnnotationMember(this)
        is DSTFieldGroup -> visitor.visitDSTFieldGroup(this)
        is DSTConstructorDeclaration -> visitor.visitDSTConstructorDeclaration(this)
        is DSTMethodDeclaration -> visitor.visitDSTMethodDeclaration(this)
        is DSTInitialiser -> visitor.visitDSTInitialiser(this)
        is DSTParameter -> visitor.visitDSTParameter(this)
        is DSTDimension -> visitor.visitDSTDimension(this)
        is DSTModifier -> visitor.visitDSTModifier(this)
        is DSTBlockStatement -> visitor.visitDSTBlockStatement(this)
        is DSTExpressionStatement -> visitor.visitDSTExpressionStatement(this)
        is DSTLocalVariableDeclarationGroup -> visitor.visitDSTLocalVariableDeclarationGroup(this)
        is DSTLocalVariableDeclaration -> visitor.visitDSTLocalVariableDeclaration(this)
        is DSTSingleVariableDeclaration -> visitor.visitDSTSingleVariableDeclaration(this)
        is DSTWhileStatement -> visitor.visitDSTWhileStatement(this)
        is DSTDoWhileStatement -> visitor.visitDSTDoWhileStatement(this)
        is DSTForStatement -> visitor.visitDSTForStatement(this)
        is DSTForEachStatement -> visitor.visitDSTForEachStatement(this)
        is DSTSuperMethodCall -> visitor.visitDSTSuperMethodCall(this)
        is DSTSuperConstructorCall -> visitor.visitDSTSuperConstructorCall(this)
        is DSTThisConstructorCall -> visitor.visitDSTThisConstructorCall(this)
        is DSTSynchronisedStatement -> visitor.visitDSTSynchronisedStatement(this)
        is DSTLabeledStatement -> visitor.visitDSTLabeledStatement(this)
        is DSTBreakStatement -> visitor.visitDSTBreakStatement(this)
        is DSTContinueStatement -> visitor.visitDSTContinueStatement(this)
        is DSTReturnStatement -> visitor.visitDSTReturnStatement(this)
        is DSTThrowStatement -> visitor.visitDSTThrowStatement(this)
        is DSTIfStatement -> visitor.visitDSTIfStatement(this)
        is DSTSwitchStatement -> visitor.visitDSTSwitchStatement(this)
        is DSTSwitchCase -> visitor.visitDSTSwitchCase(this)
        is DSTTryStatement -> visitor.visitDSTTryStatement(this)
        is DSTEmptyStatement -> visitor.visitDSTEmptyStatement(this)
        is DSTTypeDeclarationStatement -> visitor.visitDSTTypeDeclarationStatement(this)
        is DSTAssertStatement -> visitor.visitDSTAssertStatement(this)
        is DSTCatchClause -> visitor.visitDSTCatchClause(this)

        is DSTSyntheticMethodCall -> visitor.visitDSTSyntheticMethodCall(this)
        is DSTSyntheticMethodDeclaration -> visitor.visitDSTSyntheticMethodDeclaration(this)
        is DSTSyntheticField -> visitor.visitDSTSyntheticField(this)
        is DSTSyntheticName -> visitor.visitDSTSyntheticName(this)

        else -> throw IllegalArgumentException("Unknown name ${this.javaClass}")
    }
}

